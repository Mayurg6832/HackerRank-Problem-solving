def taumBday(b, w, bc, wc, z):
    total=0
    if ((b*wc)+(z*b)) < (b*bc):
        total+=((b*wc)+(z*b))
    else:
        total+=b*bc
    if ((w*bc)+(z*w)) < (w*wc):
        total+=((w*bc)+(z*w))
    else:
        total+=(w*wc)
    return total

